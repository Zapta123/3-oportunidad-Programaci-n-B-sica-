def invertir_cadena(texto):
    return texto[::-1]

print(invertir_cadena("Hola mundo"))     # Resultado: "odnum aloH"
print(invertir_cadena("Python"))         # Resultado: "nohtyP"
print(invertir_cadena(""))               # Resultado: ""
